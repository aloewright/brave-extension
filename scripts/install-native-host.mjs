#!/usr/bin/env node
/**
 * Installs the native messaging host manifest for Chrome/Brave, plus the
 * config dir, token/env file, ~/.claude.json entry, and (optionally) the
 * shell rc PATH block + claude wrapper for "available in any terminal".
 *
 *   node scripts/install-native-host.mjs [extension-id] [--enable-terminal-path]
 *
 * Idempotent — re-running converges to the same final state. The terminal-
 * path block is marker-guarded so toggling it on/off is a clean round-trip.
 */

import { writeFileSync, mkdirSync, chmodSync, existsSync } from "fs"
import { homedir } from "os"
import { join, resolve, dirname } from "path"
import { fileURLToPath } from "url"
import { spawnSync } from "child_process"
import {
  HOST_NAME,
  setTerminalPath,
  generateToken,
  writeTokenAndEnv,
  registerClaudeJson,
  tokenPath,
  envPath,
  scrubQuarantineAll,
  findNativeArtifacts,
  findExtensionIdInBrowserPreferences
} from "../native-host/installer.mjs"

const args = process.argv.slice(2)
const argExtensionId = args.find((a) => !a.startsWith("--"))
const enableTerminalPath = args.includes("--enable-terminal-path")

const __dirname = dirname(fileURLToPath(import.meta.url))
const hostScript = resolve(join(__dirname, "..", "native-host", "ai-dev-host.mjs"))
const repoRoot = resolve(join(__dirname, ".."))

// GUI-launched apps (Brave/Chrome) don't inherit the user's shell PATH, so
// `#!/usr/bin/env node` shebangs silently fail when node lives somewhere
// non-standard (nvm, fnm, asdf, vite-plus, mise, …). Write a tiny wrapper
// that hardcodes the absolute node binary that ran THIS install script.
function writeHostWrapper() {
  const wrapperPath = resolve(join(__dirname, "..", "native-host", "ai-dev-host.sh"))
  const nodeBin = process.execPath
  const wrapper = `#!/bin/bash
# Auto-generated by scripts/install-native-host.mjs.
# Re-running install regenerates this with the current node binary.
export PATH="/opt/homebrew/bin:/usr/local/bin:/usr/bin:/bin:/usr/sbin:/sbin:$PATH"
HOST_DIR="$(cd "$(dirname "$0")" && pwd)"
if [ "$(uname -s)" = "Darwin" ]; then
  for f in \\
    "$HOST_DIR"/node_modules/.pnpm/node-pty@*/node_modules/node-pty/build/Release/pty.node \\
    "$HOST_DIR"/node_modules/.pnpm/node-pty@*/node_modules/node-pty/build/Release/spawn-helper \\
    "$HOST_DIR"/node_modules/.pnpm/node-pty@*/node_modules/node-pty/prebuilds/darwin-arm64/pty.node \\
    "$HOST_DIR"/node_modules/.pnpm/node-pty@*/node_modules/node-pty/prebuilds/darwin-arm64/spawn-helper \\
    "$HOST_DIR"/node_modules/.pnpm/node-pty@*/node_modules/node-pty/prebuilds/darwin-x64/pty.node \\
    "$HOST_DIR"/node_modules/.pnpm/node-pty@*/node_modules/node-pty/prebuilds/darwin-x64/spawn-helper; do
    [ -e "$f" ] || continue
    /usr/bin/xattr -d com.apple.quarantine "$f" 2>/dev/null || true
    /usr/bin/xattr -d com.apple.provenance "$f" 2>/dev/null || true
    /usr/bin/xattr -d com.apple.macl "$f" 2>/dev/null || true
  done
fi
exec ${JSON.stringify(nodeBin)} ${JSON.stringify(hostScript)} "$@"
`
  writeFileSync(wrapperPath, wrapper)
  chmodSync(wrapperPath, 0o755)
  return wrapperPath
}

const hostPath = writeHostWrapper()
console.log(`✓ Wrote wrapper: ${hostPath}`)
console.log(`  (uses node: ${process.execPath})`)

let extensionId = argExtensionId
let detectedFrom = null
if (!extensionId) {
  // Chrome native messaging does NOT support wildcards in allowed_origins.
  // Prefer the active unpacked build path from browser Preferences/Secure
  // Preferences so a changed dev-build ID does not strand PTY/Doppler/MCP.
  const found = findExtensionIdInBrowserPreferences({ repoRoot })
  if (found) {
    extensionId = found.id
    detectedFrom = found.source
  }
}

if (!extensionId) {
  console.error("✗ No extension ID provided and none auto-detected.")
  console.error("")
  console.error("  Chrome/Brave native messaging requires the exact extension ID in the manifest.")
  console.error("  Wildcards do not work, so we won't write a manifest that's guaranteed to fail.")
  console.error("")
  console.error("  How to fix:")
  console.error("    1) Run `pnpm build` (already done if you see build/manifest.json).")
  console.error("    2) Open brave://extensions, enable Developer Mode, click 'Load unpacked',")
  console.error("       and select the build/ folder inside this repo.")
  console.error("    3) Copy the extension ID Brave assigned (long lowercase string).")
  console.error("    4) Re-run:  pnpm install-host <extension-id>")
  process.exit(1)
}

const manifest = {
  name: HOST_NAME,
  description: "Brave Dev Extension native messaging host — bridges browser to local CLI tools",
  path: hostPath,
  type: "stdio",
  allowed_origins: [`chrome-extension://${extensionId}/`]
}

if (detectedFrom) {
  console.log(`✓ Auto-detected extension ID ${extensionId}`)
  console.log(`  (from ${detectedFrom})`)
} else {
  console.log(`✓ Using extension ID ${extensionId}`)
}

const platform = process.platform
const manifestDirs = []
if (platform === "darwin") {
  manifestDirs.push(
    join(homedir(), "Library", "Application Support", "BraveSoftware", "Brave-Browser", "NativeMessagingHosts"),
    join(homedir(), "Library", "Application Support", "Google", "Chrome", "NativeMessagingHosts"),
    join(homedir(), "Library", "Application Support", "Chromium", "NativeMessagingHosts")
  )
} else if (platform === "linux") {
  manifestDirs.push(join(homedir(), ".config", "BraveSoftware", "Brave-Browser", "NativeMessagingHosts"))
} else if (platform === "win32") {
  manifestDirs.push(join(homedir(), "AppData", "Local", "AiDevSidebar", "NativeMessagingHosts"))
}

for (const d of manifestDirs) {
  try {
    mkdirSync(d, { recursive: true })
    const p = join(d, `${HOST_NAME}.json`)
    writeFileSync(p, JSON.stringify(manifest, null, 2))
    console.log(`✓ Installed manifest: ${p}`)
  } catch (e) {
    console.log(`  Skipped ${d}: ${e.message}`)
  }
}

if (platform === "win32") {
  console.log(`⚠  On Windows, also add registry key:`)
  console.log(`   HKCU\\Software\\Google\\Chrome\\NativeMessagingHosts\\${HOST_NAME}`)
}

// Make host script executable (the wrapper is already +x from writeHostWrapper)
try {
  chmodSync(hostScript, 0o755)
  console.log(`✓ Host script executable: ${hostScript}`)
} catch {}

// Install native-host npm dependencies
const hostDir = resolve(join(__dirname, "..", "native-host"))
if (existsSync(join(hostDir, "package.json"))) {
  const hasNodeModules = existsSync(join(hostDir, "node_modules"))
  if (!hasNodeModules) {
    console.log(`\nInstalling native-host dependencies in ${hostDir}…`)
    const pm = spawnSync(
      process.platform === "win32" ? "pnpm.cmd" : "pnpm",
      ["install", "--prod", "--silent"],
      { cwd: hostDir, stdio: "inherit" }
    )
    if (pm.status !== 0) {
      console.warn("⚠  pnpm install failed; falling back to npm…")
      const np = spawnSync(
        process.platform === "win32" ? "npm.cmd" : "npm",
        ["install", "--omit=dev", "--silent"],
        { cwd: hostDir, stdio: "inherit" }
      )
      if (np.status !== 0) {
        console.warn("⚠  Could not install native-host deps automatically.")
      }
    } else {
      console.log("✓ Native-host dependencies installed")
    }
  } else {
    console.log("✓ Native-host node_modules already present")
  }
}

// macOS Gatekeeper/XProtect can block node-pty's downloaded prebuild and show
// a transient hash name like `.9db7f7fbbfb777da-00000000.node`. Build node-pty
// locally so the sidebar terminal loads a machine-local native addon instead
// of a downloaded prebuild that requires a GUI "Open"/"Allow Anyway" approval.
if (process.platform === "darwin") {
  const rebuild = spawnSync(
    process.platform === "win32" ? "pnpm.cmd" : "pnpm",
    ["--dir", hostDir, "rebuild", "node-pty", "--config.build-from-source=true"],
    {
      stdio: "inherit",
      env: { ...process.env, npm_config_build_from_source: "true" }
    }
  )
  if (rebuild.status === 0) {
    console.log("✓ Rebuilt node-pty from source for this Mac")
  } else {
    console.warn("⚠  node-pty local rebuild failed; falling back to packaged prebuild.")
  }
}

// macOS: strip Gatekeeper/XProtect xattrs from every native addon under all repo
// node_modules trees (node-pty, esbuild, rollup *.node, fsevents.node, …).
// Gatekeeper popups name esbuild / rollup.darwin-arm64.node / fsevents even
// though the underlying files live under pnpm's node_modules layout. ALO-472.
//
// We do NOT re-sign. Stable ad-hoc CDHashes mean a single "Allow Anyway" in
// System Settings → Privacy & Security persists across reinstalls of the same
// package version. Re-signing would mint a new CDHash and invalidate that grant.
if (process.platform === "darwin") {
  const { scrubbed, errors } = scrubQuarantineAll(repoRoot)
  const total = scrubbed.length + errors.length
  if (total > 0) {
    console.log(
      `✓ Cleared Gatekeeper xattrs on ${scrubbed.length}/${total} native artifacts`
    )
    for (const e of errors) {
      console.warn(`  ⚠  ${e.path}: ${e.message}`)
    }
    console.log(
      "  If Gatekeeper still pops on dev/build or first terminal use, run\n" +
        "  `pnpm rebuild-pty` to rebuild node-pty locally and scrub again.\n" +
        "  Run `pnpm diagnose-host` to see per-file signing/quarantine state."
    )
  }
  const warm = spawnSync(process.execPath, [join(repoRoot, "scripts/warm-node-pty.mjs")], {
    stdio: "inherit",
    env: process.env
  })
  if (warm.status !== 0) {
    console.warn("⚠  node-pty warm-load did not complete — sidebar terminal may prompt Gatekeeper on first use.")
  }
}

// Token + env file (placeholder port — real port is chosen at MCP server
// start; the host rotates the token and rewrites these files at startup).
// We pre-seed so that ~/.claude.json registration has something to point at
// and so terminals launched before the host first runs still see env vars.
if (!existsSync(tokenPath()) || !existsSync(envPath())) {
  const token = generateToken()
  writeTokenAndEnv(token, 8473)
  console.log(`✓ Wrote ~/.config/ai-dev-sidebar/{mcp-token,env}`)
} else {
  console.log(`✓ Token + env already present (rotated on host start)`)
}

// ~/.claude.json registration (port 8473 default; host updates if it picks
// a different port from PORT_RANGE).
try {
  registerClaudeJson(8473)
  console.log(`✓ Registered ai-dev-sidebar in ~/.claude.json`)
} catch (err) {
  console.warn(`⚠  Could not write ~/.claude.json: ${err.message}`)
}

// Optional: terminal path block + wrapper
if (enableTerminalPath) {
  const results = setTerminalPath(true)
  for (const r of results) {
    console.log(`${r.changed ? "✓" : "·"} ${r.path}`)
  }
  console.log(`\n  "Available in any terminal" enabled. Restart your shell or run:`)
  console.log(`    source ~/.zshrc  # or ~/.bashrc`)
}

console.log("\nDone! Restart your browser for changes to take effect.")
